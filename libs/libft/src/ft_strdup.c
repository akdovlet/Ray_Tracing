#include "libft.h"
#include <stdio.h>

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*dup;

	i = 0;
	if (!s)
		return (NULL);
	dup = malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!dup)
		return (NULL);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
