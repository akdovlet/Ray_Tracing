#include "minirt.h"

t_matrix	scale(t_tuple t1)
{
	return ((t_matrix){{
			{t1.x, 0, 0, 0}, \
			{0, t1.y, 0, 0}, \
			{0, 0, t1.z, 0}, \
			{0, 0, 0, 1} \
	}});
}

t_matrix	translate(t_tuple t1)
{
	return ((t_matrix){{
			{1, 0, 0, t1.x}, \
			{0, 1, 0, t1.y}, \
			{0, 0, 1, t1.z}, \
			{0, 0, 0, 1} \
	}});
}
