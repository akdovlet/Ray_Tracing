#include "minirt.h"

inline v4	tuple_cross(v4 a, v4 b)
{
	return ((v4){
		(a[1] * b[2]) - (a[2] * b[1]),
		(a[2] * b[0]) - (a[0] * b[2]),
		(a[0] * b[1]) - (a[1] * b[0])
	});
}
