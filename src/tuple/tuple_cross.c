#include "minirt.h"

t_tuple	tuple_cross(t_tuple a, t_tuple b)
{
	return ((t_tuple){
		(a[1] * b[2]) - (a[2] * b[1]),
		(a[2] * b[0]) - (a[0] * b[2]),
		(a[0] * b[1]) - (a[1] * b[0]),
	});
}
